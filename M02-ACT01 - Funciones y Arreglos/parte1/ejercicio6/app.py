def invertir_cadena(cadena): #Funcion
    return(cadena[::-1]) #Devuelve la cadena de texto invertida, [::-1] es el comando que la invierte.

resultado = invertir_cadena(cadena = "Cadena Invertida") #Se otorga el resultado de la funcion a la variable y se define la palabra que va a ser invertida.
print(f"La cadena invertida es: {resultado} ") #Salida del programa.