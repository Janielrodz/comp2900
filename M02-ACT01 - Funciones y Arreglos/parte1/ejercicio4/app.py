def promedio(lista_numeros): #funcion
    return sum(lista_numeros) / len(lista_numeros) #devuelve el resultado de la suma de los numeros de la lista sobre la cantidad de numeros en la lista.

lista_numeros = [2, 4, 6, 8, 10] #Lista de numeros
resultadoFinal = promedio(lista_numeros) #Se le otorga el resultado de la funcion a la variable.
print(f"El promedio de la lista de números sería: {resultadoFinal}") #Salida del programa.


